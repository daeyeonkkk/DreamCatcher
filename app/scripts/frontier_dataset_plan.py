#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

APP_ROOT = Path(__file__).resolve().parents[1]
REPO_ROOT = Path(__file__).resolve().parents[2]
if str(APP_ROOT) not in sys.path:
    sys.path.insert(0, str(APP_ROOT))

from backend.app.core.frontier_dataset_catalog import build_frontier_dataset_plan  # noqa: E402


def _resolve_path(value: str | None, *, default: Path) -> Path:
    if not value:
        return default
    path = Path(value)
    return path if path.is_absolute() else (Path.cwd() / path)


def _download_script_text(plan: dict[str, object]) -> str:
    lines = [
        "#!/usr/bin/env bash",
        "set -euo pipefail",
        "",
        "# Generated by app/scripts/frontier_dataset_plan.py.",
        "# Review licenses, gated access, and storage budget before running any command.",
        "",
    ]
    for item in plan.get("download_plan", []):
        if not isinstance(item, dict):
            continue
        dataset_id = item.get("dataset_id")
        status = item.get("status")
        command = item.get("command")
        manual_url = item.get("manual_url")
        lines.append(f"# {dataset_id}: {status}")
        if command:
            lines.append(str(command))
        elif manual_url:
            lines.append(f"# manual: {manual_url}")
        else:
            lines.append("# metadata-only: no download command")
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Plan Frontier dataset/research collection without downloading corpora by default."
    )
    parser.add_argument("--seed-root", default=str(REPO_ROOT / "seed_bundle"), help="Path to the seed_bundle root.")
    parser.add_argument("--task", action="append", default=[], help="Task id to include; repeatable.")
    parser.add_argument("--dataset-id", action="append", default=[], help="Dataset id to include; repeatable.")
    parser.add_argument(
        "--local-cache-root",
        default=None,
        help="Override dataset cache root; defaults to the manifest policy local_cache_root.",
    )
    parser.add_argument("--out", default=None, help="Optional JSON output path.")
    parser.add_argument(
        "--download-script-out",
        default=None,
        help="Optional shell plan path with commands/comments. It is not executed.",
    )
    parser.add_argument(
        "--require-task",
        action="append",
        default=[],
        help="Fail if the planned coverage does not include this task id; repeatable.",
    )
    args = parser.parse_args()

    seed_root = _resolve_path(args.seed_root, default=REPO_ROOT / "seed_bundle")
    cache_root = _resolve_path(args.local_cache_root, default=Path()) if args.local_cache_root else None
    plan = build_frontier_dataset_plan(
        seed_root=seed_root,
        tasks=args.task,
        dataset_ids=args.dataset_id,
        local_cache_root=cache_root,
    )
    payload = plan.to_dict()

    required_missing = [
        task
        for task in args.require_task
        if task not in payload["task_coverage"] or not payload["task_coverage"][task]
    ]
    if required_missing:
        payload["ok"] = False
        payload["missing_required_tasks"] = required_missing

    text = json.dumps(payload, ensure_ascii=False, indent=2)
    print(text)

    if args.out:
        out_path = _resolve_path(args.out, default=Path.cwd() / "frontier_dataset_plan.json")
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(text + "\n", encoding="utf-8")

    if args.download_script_out:
        script_path = _resolve_path(args.download_script_out, default=Path.cwd() / "frontier_dataset_download_plan.sh")
        script_path.parent.mkdir(parents=True, exist_ok=True)
        script_path.write_text(_download_script_text(payload), encoding="utf-8")

    if payload["ok"] is not True:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
